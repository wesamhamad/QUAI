def set_rtl_paragraph(paragraph):
    """Set paragraph to RTL direction."""
    pPr = paragraph._p.get_or_add_pPr()
    bidi = parse_xml(f'<w:bidi {nsdecls("w")} val="1"/>')
    pPr.append(bidi)

def set_rtl_run(run):
    """Set run to RTL direction."""
    rPr = run._r.get_or_add_rPr()
    rtl = parse_xml(f'<w:rtl {nsdecls("w")} val="1"/>')
    rPr.append(rtl)

def set_cell_rtl(cell):
    """Set table cell to RTL."""
    for paragraph in cell.paragraphs:
        set_rtl_paragraph(paragraph)

def add_rtl_paragraph(doc, text, style='Normal', bold=False, size=12, color=None, alignment=WD_ALIGN_PARAGRAPH.RIGHT):
    """Add RTL Arabic paragraph."""
    p = doc.add_paragraph(style=style)
    p.alignment = alignment
    set_rtl_paragraph(p)
    run = p.add_run(text)
    run.font.size = Pt(size)
    run.font.name = 'Arial'
    run.bold = bold
    if color:
        run.font.color.rgb = color
    set_rtl_run(run)
    # Set font for complex script (Arabic)
    rPr = run._r.get_or_add_rPr()
    cs_font = parse_xml(f'<w:rFonts {nsdecls("w")} w:cs="Arial"/>')
    rPr.append(cs_font)
    cs_size = parse_xml(f'<w:szCs {nsdecls("w")} w:val="{size * 2}"/>')
    rPr.append(cs_size)
    return p

def add_heading_rtl(doc, text, level=1):
    """Add RTL heading."""
    h = doc.add_heading(text, level=level)
    h.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    set_rtl_paragraph(h)
    for run in h.runs:
        set_rtl_run(run)
        run.font.name = 'Arial'
        rPr = run._r.get_or_add_rPr()
        cs_font = parse_xml(f'<w:rFonts {nsdecls("w")} w:cs="Arial"/>')
        rPr.append(cs_font)
    return h

def add_table_rtl(doc, headers, rows, col_widths=None):
    """Add RTL table with headers and rows."""
    table = doc.add_table(rows=1 + len(rows), cols=len(headers))
    table.style = 'Table Grid'
    table.alignment = WD_TABLE_ALIGNMENT.CENTER

    # Set table RTL
    tblPr = table._tbl.tblPr
    bidi = parse_xml(f'<w:bidiVisual {nsdecls("w")} val="1"/>')
    tblPr.append(bidi)

    # Header row
    hdr_cells = table.rows[0].cells
    for i, header in enumerate(headers):
        hdr_cells[i].text = ''
        p = hdr_cells[i].paragraphs[0]
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        set_rtl_paragraph(p)
        run = p.add_run(header)
        run.bold = True
        run.font.size = Pt(10)
        run.font.name = 'Arial'
        run.font.color.rgb = RGBColor(255, 255, 255)
        set_rtl_run(run)
        # Blue background
        shading = parse_xml(f'<w:shd {nsdecls("w")} w:fill="1F4E79" w:val="clear"/>')
        hdr_cells[i]._tc.get_or_add_tcPr().append(shading)

    # Data rows
    for row_idx, row_data in enumerate(rows):
        cells = table.rows[row_idx + 1].cells
        for col_idx, cell_text in enumerate(row_data):
            cells[col_idx].text = ''
            p = cells[col_idx].paragraphs[0]
            p.alignment = WD_ALIGN_PARAGRAPH.CENTER
            set_rtl_paragraph(p)
            run = p.add_run(str(cell_text))
            run.font.size = Pt(9)
            run.font.name = 'Arial'
            set_rtl_run(run)
            # Alternating row colors
            if row_idx % 2 == 0:
                shading = parse_xml(f'<w:shd {nsdecls("w")} w:fill="E8F0FE" w:val="clear"/>')
                cells[col_idx]._tc.get_or_add_tcPr().append(shading)

    return table

